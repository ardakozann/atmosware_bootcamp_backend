// Yazılım Prensible;

// Clean Code:
// Gereksiz verilerden,tekrarlardan,okunabiliği yüksek, yazılabilirliği daha
// Kodun temiz olması, kodu yazan geliştirici dışında ekiptekilerin
// kodu kolay şekilde anlayabilmesi ve geliştirme yapabilmesidir.
// readability, (Basitçe okunup anlaşılıyor )
// changeability, (Basitçe değiştirilebiliyor )
// extensibility (Basitçe genişletilebiliyor )
// maintainability. (Basitçe bakım yapılabilsin)
// isimlendirme: net bir şekilde belirmemiz lazım.

// design pattern, software pattern , SOLID ,

// Spagetti Codes:
// Clean Code tam tersi durumuna denir.
// Gereksiz, okunabilirliği zor, düzensiz iç içe girmiş for-while.vs olan
// kodlara denir.

// KISS (Keep It Simple Stupid)
// Kodlarımızda her zaman için en basit yolla sonuça varmaktır.
// Kolay bir yol ile çözülebilecek problemleri ya da başka bir ifade ile,
// bir kaç satır kod ile geliştirilebilecek bir metodu, gereksiz ifadelerden
// kaçınmaktır.

// DRY (Don't Repeat Yourself)
// Kodlarımızda sürekli kendimizi tekrar yapmamız gereksiz ve sakıncalıdır.
// Çözüm: Class, Metotları

// Dammy Code (Kukla Kod)
// Classlarımızın içinde interface'leri implement ederek , eklenen gereksiz ve
// kullanılmayan kod bütüne denir.

// YAGNI – You Ain't Gonna Need It(Buna İhtiyacın Olmayacak)
// YAGNI, ihtiyacımız olmayacak şeyleri sisteme dahil etmemeyi söyleyen bir
// prensip.

// RREP:Reuse-Release Equivalence Principle (REP) –
// Tekrar Kullanım ve Sürüm Eşitliği
// Projemizde paketlemeler çok öenmlidir
// Classlarımızı paketler yardımıyla saklarız.
// Common roles: paketler küçük harflerle yazılır.

// CCP: Common Clouse Prensible
// ilgili classı, ilgili pakete eklemek gerekir.

