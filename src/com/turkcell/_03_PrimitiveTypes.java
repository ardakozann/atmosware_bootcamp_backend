package com.turkcell;

public class _03_PrimitiveTypes {
	
	public static void main(String[] args) {
		// pritimite types: stack memory'de toplanır: hafıza uzayı belli olan
		
		// tam sayılar
		byte b1 = 44;
		short s1 = 129;
		int i1 = 444;
		long l1 = 1L + 2L;
		
		// virgüllü sayılar
		float f2 = 44.55f;
		double d2 = 44.56;
		
		boolean b3 = 3 > 1;
		char c3 = '@';
		System.out.println(c3);
		
		// bilimsel gösterim
		double bilimsel = 2E+3;
		System.out.println(bilimsel);
		
		int cellPhone1 = 1112233;
		System.out.println(cellPhone1);
		
		int cellPhone2 = 111_22_33;
		System.out.println(cellPhone2);
		
		// Primitive Types Wrapper class
		// byte Byte
		// short Short
		//
	}
}
